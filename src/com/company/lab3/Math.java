package com.company.lab3;

public class Math {
    public int sum(int firstNumber, int secondNumber){

        return(firstNumber + secondNumber);
    }
    public double sum(double firstNumber, double secondNumber) {

        return (firstNumber + secondNumber);

    }
    public int sum(int firstNumber, int secondNumber,int thirdNumber) {

        return (firstNumber + secondNumber+thirdNumber);
    }

    public int sub(int firstNumber, int secondNumber) {

        return (firstNumber - secondNumber);
    }
    public double sub(double firstNumber, int secondNumber) {

        return (firstNumber - secondNumber);
    }
    public int sub(int firstNumber, int secondNumber,int thirdNumber) {

        return (firstNumber - secondNumber-thirdNumber);
    }

    public int div(int firstNumber, int secondNumber) {

        return (firstNumber / secondNumber);
    }
    public double div(double firstNumber, int secondNumber) {

        return (firstNumber / secondNumber);
    }

    public int times(int firstNumber, int secondNumber){

        return( firstNumber*secondNumber);
    }
    public int times(int firstNumber, int secondNumber,int thirdNumber){

        return( firstNumber*secondNumber*thirdNumber);
    }

    public int rest(int firstNumber, int secondNumber){

        return(firstNumber%secondNumber);
    }
    public double rest(double firstNumber, int secondNumber){

        return(firstNumber%secondNumber);
    }

    public double real(double firstNumber, double secondNumber){

        return(firstNumber/secondNumber);
    }
    public int real(int firstNumber, int secondNumber){

        return(firstNumber/secondNumber);
    }

    public double bigsum(int firstNumber, int secondNumber,double thirdNumber){
        return(firstNumber+secondNumber+thirdNumber);
    }
    public int bigsum(int firstNumber, int secondNumber,int thirdNumber){
        return(firstNumber+secondNumber+thirdNumber);
    }

    public int dif(int firstNumber, int secondNumber){
        return(firstNumber-secondNumber);
    }
    public int dif(int firstNumber, int secondNumber,int thirdNumber){
        return(firstNumber-secondNumber-thirdNumber);
    }

    public double imp(double firstNumber, double secondNumber, double thirdNumber){
        return(firstNumber/secondNumber/thirdNumber);
    }
    public int imp(int firstNumber, int secondNumber, int thirdNumber){
        return(firstNumber/secondNumber/thirdNumber);
    }

    public double a(double firstNumber, double secondNumber, double thirdNumber){
        return(firstNumber/secondNumber+thirdNumber);
    }
    public double a(double firstNumber, double secondNumber, double thirdNumber,double fourthNumber){
        return(firstNumber/secondNumber+thirdNumber/fourthNumber);
    }

}
