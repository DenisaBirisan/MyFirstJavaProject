package com.company.lab3;

public class Math {
    public int sum(int firstNumber, int secondNumber){

        return(firstNumber + secondNumber);
    }

    public int sub(int firstNumber, int secondNumber) {

        return (firstNumber - secondNumber);
    }

    public int div(int firstNumber, int secondNumber) {

        return (firstNumber / secondNumber);
    }

    public int times(int firstNumber, int secondNumber){

        return( firstNumber*secondNumber);
    }

    public int rest(int firstNumber, int secondNumber){

        return(firstNumber%secondNumber);
    }

    public double real(double firstNumber, double secondNumber){

        return(firstNumber/secondNumber);
    }

    public double bigsum(int firstNumber, int secondNumber,double thirdNumber){
        return(firstNumber+secondNumber+thirdNumber);
    }

    public int dif(int firstNumber, int secondNumber){
        return(firstNumber-secondNumber);
    }

    public double imp(double firstNumber, double secondNumber, double thirdNumber){
        return(firstNumber/secondNumber/thirdNumber);
    }

    public double a(double firstNumber, double secondNumber, double thirdNumber){
        return(firstNumber/secondNumber+thirdNumber);
    }

}
