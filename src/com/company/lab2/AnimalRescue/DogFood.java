package com.company.lab2.AnimalRescue;

public class DogFood {
    private String dogFoodName;
    private double dogFoodPrice;
    private double dogFoodQty;
    private String dogFoodStore;
    private String dogFoodComposition;
    private String dogFoodAvailability;

    public String getDogFoodName(){
        return dogFoodName;
    }
    public void setDogFoodName(String dogFoodName){
        this.dogFoodName=dogFoodName;
    }

    public double getDogFoodPrice(){
        return dogFoodPrice;
    }
    public void setDogFoodPrice(double dogFoodPrice){
        this.dogFoodPrice=dogFoodPrice;
    }

    public double getDogFoodQty(){
        return dogFoodQty;
    }
    public void setDogFoodQty(double dogFoodQty){
        this.dogFoodQty=dogFoodQty;
    }

    public String getDogFoodStore(){
        return dogFoodStore;
    }
    public void setDogFoodStore(String dogFoodStore){
        this.dogFoodStore=dogFoodStore;
    }

    public String getDogFoodComposition(){
        return dogFoodComposition;
    }
    public void setDogFoodComposition(String dogFoodComposition){
        this.dogFoodComposition=dogFoodComposition;
    }

    public String getDogFoodAvailability(){
        return dogFoodAvailability;
    }
    public void setDogFoodAvailability(String dogFoodAvailability){
        this.dogFoodAvailability=dogFoodAvailability;
    }

    }


